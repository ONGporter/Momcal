/**
 * @typedef {'height'|'weight'|'head'} GrowthMetric
 * @typedef {'m'|'f'|'u'} ChildGender
 * @typedef {{ m:number, L:number, M:number, S:number }} LMSPoint
 */

/**
 * 성별 문자열('m'|'f'|'u') → 참고 테이블(LMS)
 * 'u'(성별 미정)인 경우 남아/여아 LMS를 평균하여 사용 (화면에 "평균 기준" 안내 필요)
 */
export function refTableFor(gender) {
  if (gender === 'm') return boys;
  if (gender === 'f') return girls;
  // 성별 미정 → 남/여 LMS 평균 테이블 동적 생성
  const avg = {};
  ['height', 'weight', 'head'].forEach(metric => {
    avg[metric] = boys[metric].map((b, i) => {
      const g = girls[metric][i];
      return {
        m: b.m,
        L: +((b.L + g.L) / 2).toFixed(4),
        M: +((b.M + g.M) / 2).toFixed(2),
        S: +((b.S + g.S) / 2).toFixed(5),
      };
    });
  });
  return avg;
}

