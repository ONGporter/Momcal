/**
 * data/who-growth.js
 * 성장 백분위 계산용 참고 데이터 — WHO Child Growth Standards LMS 방식
 *
 * ── 이번 개편(정교화) 요약 ────────────────────────────────────────────
 * 예전에는 "중앙값 ± 표준편차"의 정규분포 근사로 백분위를 계산했습니다. 하지만 WHO 공식
 * 성장 표준은 좌우로 치우친(비대칭) 분포를 LMS(Box-Cox power) 방식으로 다루기 때문에,
 * 특히 몸무게처럼 분포가 한쪽으로 기운 지표는 정규분포 근사와 실제 성장곡선의 상·하위
 * 구간에서 차이가 났습니다. 그래서 계산 방식을 WHO가 쓰는 LMS z-점수 공식으로 바꿨습니다.
 *
 *   z = ((X / M)^L − 1) / (L · S)         (L ≠ 0)
 *   z = ln(X / M) / S                     (L = 0)
 *   특정 백분위 값 X = M · (1 + L·S·z)^(1/L)   (L ≠ 0)
 *                 X = M · exp(S·z)            (L = 0)
 *
 * ── 데이터 구성 ──────────────────────────────────────────────────────
 * 각 항목은 { m: 개월수, L, M(중앙값=P50), S } 형태입니다.
 *  · 키(height)·머리둘레(head): WHO 표준에서 L = 1 (거의 정규분포)이라 L=1로 두었고,
 *    S는 검증돼 있던 기존 중앙값·표준편차로부터 S = sd / M 으로 환산했습니다.
 *    → 이 두 지표는 기존 계산 결과와 사실상 동일하게 유지되며(원래도 정규에 가까웠음),
 *      계산 틀만 LMS로 통일했습니다.
 *  · 몸무게(weight): 비대칭이 큰 지표라 WHO LMS의 L·S 파라미터를 실제로 반영했습니다.
 *    중앙값 M은 기존에 검증된 WHO P50 값을 그대로 유지했습니다.
 *
 * ── 정확도에 대한 정직한 안내 ────────────────────────────────────────
 * 이 표는 WHO가 공개하는 대표 월령(anchor age)들의 LMS 값을 담고, 그 사이 개월수는
 * 선형 보간합니다. WHO가 배포하는 "0~60개월 1개월 단위 공식 CSV" 원본을 그대로 넣으면
 * 임상 수준까지 정밀해지는데, 그때는 아래 `boys`/`girls` 표의 행만 촘촘하게 교체하면 되고
 * js/growthChart.js 의 계산 로직은 손댈 필요가 없도록 { m, L, M, S } 구조를 유지했습니다.
 * 화면에는 항상 "참고용, 의학적 진단 아님" 문구를 함께 표시합니다.
 */

